//---------------------------------------------------------------------------
void CDiskMakeDlg::OnDestroy()
{
	// スレッド終了
	if (m_pThread) {
		// フラグを上げ、終了を待つ(無期限)
		m_bThread = TRUE;
		::WaitForSingleObject(m_pThread->m_hThread, INFINITE);
		m_pThread = NULL;
	}

	// タイマ終了
	if (m_nTimerID) {
		// タイマ削除
		KillTimer(m_nTimerID);
		m_nTimerID = NULL;
	}

	// バッファ解放
	if (m_pBuffer) {
		delete[] m_pBuffer;
		m_pBuffer = NULL;
	}

	// 基本クラス
	CDialog::OnDestroy();
}

//---------------------------------------------------------------------------
//
//	タイマ
//
//---------------------------------------------------------------------------
void CDiskMakeDlg::OnTimer(UINT /*nTimerID*/)
{
	CString strParcent;
	DWORD dwCurrent;
	DWORD dwParcent;
	DWORD dwTemp;
	CStatic *pStatic;
	CProgressCtrl *pProgress;

	// タイマを消す
	KillTimer(m_nTimerID);
	m_nTimerID = NULL;

	// 現在の書き込みサイズを取得(クリティカルセクションを挟む)
	m_CSection.Lock();
	dwCurrent = m_dwCurrent;
	m_CSection.Unlock();

	// パーセンテージを作る
	dwTemp = m_dwSize >> 16;
	dwParcent = m_dwCurrent >> 16;
	dwParcent = (dwParcent * 100) / dwTemp;

	// 現在と一致していなければ更新
	if (m_dwParcent != dwParcent) {
		// ワークを更新
		m_dwParcent = dwParcent;

		// 文字列を作る
		if (m_dwSize & 0xfffff) {
			strParcent.Format(_T("%dMB / %d.%1dMB (%d%%)"),
							dwCurrent >> 20,
							m_dwSize >> 20,
							(m_dwSize & 0xfffff) / 104858,
							dwParcent);
		}
		else {
			strParcent.Format(_T("%dMB / %dMB (%d%%)"),
							dwCurrent >> 20,
							m_dwSize >> 20,
							dwParcent);
		}

		// スタティックを更新
		pStatic = (CStatic*)GetDlgItem(IDC_DMAKE_STATIC);
		ASSERT(pStatic);
		pStatic->SetWindowText(strParcent);

		// プログレスを更新
		pProgress = (CProgressCtrl*)GetDlgItem(IDC_DMAKE_PROGRESS);
		ASSERT(pProgress);
		pProgress->SetPos(dwParcent);
	}

	// 改めてタイマを設定
	m_nTimerID = SetTimer(IDD_DMAKEDLG, 200, NULL);

	// スレッドがなくなっていれば、ダイアログを終了させる
	if (!m_pThread) {
		EndDialog(IDOK);
	}
}

