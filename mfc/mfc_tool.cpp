{
	Fileio fio;
	CFrmWnd *pFrmWnd;

	// ファイル名がなければ終了
	if (_tcslen(m_szPath) == 0) {
		// 基本クラス
		CDialog::OnOK();
		return;
	}

	// ファイル作成を試みる
	if (!fio.Open(m_szPath, Fileio::WriteOnly)) {
		// 基本クラス
		CDialog::OnOK();
		return;
	}
	// 一旦閉じる
	fio.Close();

	// VMを止める(状態記憶および再開はコマンドハンドラで行なう)
	pFrmWnd = (CFrmWnd*)GetParent();
	pFrmWnd->GetScheduler()->Enable(FALSE);

	// 振り分け
	switch (m_nType) {
		// SASI-HD
		case 0:
			CreateSASI();
			break;

		// SCSI-HD
		case 1:
			CreateSCSI();
			break;

		// SCSI-MO
		case 2:
			CreateMO();
			break;

		// その他(あり得ない)
		default:
			CDialog::OnOK();
			return;
	}

	// 基本クラス
	CDialog::OnOK();
}

