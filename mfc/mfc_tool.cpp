//---------------------------------------------------------------------------
//
//	ダイアログ初期化
//
//---------------------------------------------------------------------------
BOOL CFDIDlg::OnInitDialog()
{
	CButton *pButton;
	CEdit *pEdit;

	// 基本クラス
	CDialog::OnInitDialog();

	// ファイルを聞く
	if (!GetFile()) {
		EndDialog(IDCANCEL);
		return FALSE;
	}

	// ディスク名
	pEdit = (CEdit*)GetDlgItem(IDC_FDI_NAMEE);
	ASSERT(pEdit);
	if (m_dwType == 2) {
		// 最初のファイル選択でD68が選ばれた場合のみ
		_tcscpy(m_szDiskName, _T("Default"));
	}
	pEdit->SetWindowText(m_szDiskName);
	MaskName();

	// 物理フォーマット
	SetPhysical();
	MaskPhysical();

	// 論理フォーマット
	SetLogical();
	MaskLogical();

	// マウント
	switch (m_nDrive) {
		// ドライブ0
		case 0:
			pButton = (CButton*)GetDlgItem(IDC_FDI_DRIVE0);
			break;

		// ドライブ1
		case 1:
			pButton = (CButton*)GetDlgItem(IDC_FDI_DRIVE1);
			break;

		// マウントしない
		default:
			pButton = (CButton*)GetDlgItem(IDC_FDI_NOTMOUNT);
			break;
	}
	ASSERT(pButton);
	pButton->SetCheck(1);

	return TRUE;
}

//---------------------------------------------------------------------------
//
//	ダイアログOK
//
//---------------------------------------------------------------------------
void CFDIDlg::OnOK()
{
	CButton *pButton;
	CEdit *pEdit;

	// ディスク名
	pEdit = (CEdit*)GetDlgItem(IDC_FDI_NAMEE);
	ASSERT(pEdit);
	pEdit->GetWindowText(m_szDiskName, sizeof(m_szDiskName));
	if (m_dwType == 2) {
		// D88は16文字まで
		m_szDiskName[16] = _T('\0');
	}
	else {
		// DIMは59文字まで
		m_szDiskName[59] = _T('\0');
	}

	// データ取得
	GetPhysical();
	GetLogical();

	// マウント
	m_nDrive = -1;
	pButton = (CButton*)GetDlgItem(IDC_FDI_DRIVE0);
	ASSERT(pButton);
	if (pButton->GetCheck() == 1) {
		m_nDrive = 0;
	}
	pButton = (CButton*)GetDlgItem(IDC_FDI_DRIVE1);
	ASSERT(pButton);
	if (pButton->GetCheck() == 1) {
		m_nDrive = 1;
	}

	// 基本クラス
	CDialog::OnOK();
}

